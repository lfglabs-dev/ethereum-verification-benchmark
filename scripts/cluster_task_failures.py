#!/usr/bin/env python3
"""Cluster Verity tasks by difficulty and shared failure structure, and render a report.

Consumes ``analysis/task_features.json`` (produced by ``extract_task_features.py``) and
emits ``analysis/reports/hard_tasks.md``: a per-version view of which proof problems and
failure modes models struggle with.

"Clustering" here is deterministic and explainable rather than statistical: tasks are
grouped by their exact pass/fail signature across the comparison cohort, so a reader can
see precisely which models a cluster separates. This is more reproducible than k-means
over a 135-row matrix and avoids spurious centroids on a small model set.
"""
from __future__ import annotations

import argparse
import json
import sys
from collections import Counter, defaultdict
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parent.parent
DEFAULT_FEATURES = ROOT / "analysis" / "task_features.json"
DEFAULT_FAILURE_MODES = ROOT / "analysis" / "failure_modes.json"
DEFAULT_REPORT = ROOT / "analysis" / "reports" / "hard_tasks.md"


def load_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def short_model(model_id: str) -> str:
    return model_id.split("/")[-1]


def pct(value: float | None) -> str:
    return "n/a" if value is None else f"{value * 100:.0f}%"


def group_rate(tasks: list[dict[str, Any]], key: str) -> list[tuple[str, int, int, float]]:
    """Return (group_value, passes, attempts, pass_rate) rows sorted by ascending rate."""
    passes: Counter[str] = Counter()
    attempts: Counter[str] = Counter()
    for task in tasks:
        value = str(task.get(key) or "unknown")
        passes[value] += int(task.get("passes") or 0)
        attempts[value] += int(task.get("attempts") or 0)
    rows = [
        (value, passes[value], attempts[value], (passes[value] / attempts[value] if attempts[value] else 0.0))
        for value in attempts
    ]
    return sorted(rows, key=lambda r: (r[3], -r[2], r[0]))


def signature(task: dict[str, Any], cohort: list[str]) -> tuple[str, ...]:
    parts = []
    for model in cohort:
        detail = task["per_model"].get(model)
        if detail is None:
            parts.append("-")
        else:
            parts.append("P" if detail["passed"] else "F")
    return tuple(parts)


def render_report(features: dict[str, Any], failure_modes: dict[str, Any]) -> str:
    tasks = features["tasks"]
    cohort = features["cohort"]
    mode_labels = {m["id"]: m["label"] for m in failure_modes.get("lean_failure_modes", [])}
    mode_labels.update({s["id"]: s["label"] for s in failure_modes.get("outcome_statuses", [])})

    lines: list[str] = []
    lines.append(f"# Hard & divisive tasks — benchmark v{features.get('benchmark_version')}")
    lines.append("")
    lines.append("> Generated by `scripts/cluster_task_failures.py` from `analysis/task_features.json`.")
    lines.append("> Do not edit by hand; regenerate after refreshing features.")
    lines.append("")

    total_attempts = sum(t["attempts"] for t in tasks)
    total_passes = sum(t["passes"] for t in tasks)
    unsolved = [t for t in tasks if t["cohort_pass_rate"] == 0.0]
    overall = total_passes / total_attempts if total_attempts else 0.0
    lines.append("## Overview")
    lines.append("")
    lines.append(f"- Tasks: **{len(tasks)}**")
    lines.append(f"- Comparison cohort ({len(cohort)} full-coverage models): {', '.join(short_model(m) for m in cohort)}")
    lines.append(f"- Total valid attempts (all models): {total_attempts}; overall pass rate: **{pct(overall)}**")
    lines.append(f"- Tasks no cohort model solved: **{len(unsolved)}** / {len(tasks)}")
    if features.get("enrichment_present"):
        lines.append(f"- Failure-mode enrichment present for {features['enriched_pairs']} (model, task) pairs")
    else:
        lines.append("- Failure-mode enrichment: _not present_ (run with `--runs-dir` to populate)")
    lines.append("")

    lines.append("## Pass rate by proof family")
    lines.append("")
    lines.append("| Proof family | Pass rate | Passes / attempts |")
    lines.append("| --- | --- | --- |")
    for value, p, a, rate in group_rate(tasks, "proof_family"):
        lines.append(f"| {value} | {pct(rate)} | {p} / {a} |")
    lines.append("")

    lines.append("## Pass rate by author-assigned difficulty")
    lines.append("")
    lines.append("Empirical check on the a-priori `difficulty` label: harder labels should track lower pass rates.")
    lines.append("")
    lines.append("| Difficulty | Pass rate | Passes / attempts |")
    lines.append("| --- | --- | --- |")
    difficulty_order = {"easy": 0, "medium": 1, "hard": 2}
    for value, p, a, rate in sorted(group_rate(tasks, "difficulty"), key=lambda r: difficulty_order.get(r[0], 9)):
        lines.append(f"| {value} | {pct(rate)} | {p} / {a} |")
    lines.append("")

    lines.append("## Hardest tasks (no cohort model solved)")
    lines.append("")
    lines.append("Sorted by mean token spend across attempts — high spend with zero success marks the most")
    lines.append("expensive dead ends.")
    lines.append("")
    lines.append("| Task | Proof family | Difficulty | Attempts | Mean tokens | Top failure mode |")
    lines.append("| --- | --- | --- | --- | --- | --- |")
    hardest = sorted(unsolved, key=lambda t: -(t["mean_total_tokens"] or 0))[:20]
    for task in hardest:
        fm = task.get("failure_modes") or {}
        top = max(fm.items(), key=lambda kv: kv[1])[0] if fm else "—"
        top_label = mode_labels.get(top, top)
        mean_tok = f"{int(task['mean_total_tokens']):,}" if task["mean_total_tokens"] else "—"
        lines.append(
            f"| `{task['task_ref']}` | {task.get('proof_family')} | {task.get('difficulty')} "
            f"| {task['attempts']} | {mean_tok} | {top_label} |"
        )
    lines.append("")

    lines.append("## Cohort pass/fail signature clusters")
    lines.append("")
    lines.append(f"Each task's outcome across the cohort `[{', '.join(short_model(m) for m in cohort)}]` "
                 "(`P`=pass, `F`=fail, `-`=no attempt). Clusters with mixed P/F are where comparable "
                 "models genuinely disagree.")
    lines.append("")
    clusters: dict[tuple[str, ...], list[dict[str, Any]]] = defaultdict(list)
    for task in tasks:
        clusters[signature(task, cohort)].append(task)
    lines.append("| Signature | Tasks | Example |")
    lines.append("| --- | --- | --- |")
    for sig, group in sorted(clusters.items(), key=lambda kv: (-sum(1 for x in kv[0] if x == "P"), -len(kv[1]))):
        sig_str = " ".join(sig)
        example = sorted(group, key=lambda t: t["task_ref"])[0]["task_ref"]
        lines.append(f"| `{sig_str}` | {len(group)} | `{example}` |")
    lines.append("")

    divisive = sorted(
        (t for t in tasks if t["cohort_pass_rate"] not in (None, 0.0, 1.0)),
        key=lambda t: (-(t["divisiveness"] or 0), -(t["mean_total_tokens"] or 0)),
    )[:15]
    lines.append("## Most divisive tasks")
    lines.append("")
    lines.append("Some cohort models pass, others fail — the highest-signal tasks for differentiating models.")
    lines.append("")
    lines.append("| Task | Proof family | Difficulty | Cohort signature | Pass rate |")
    lines.append("| --- | --- | --- | --- | --- |")
    for task in divisive:
        sig_str = " ".join(signature(task, cohort))
        lines.append(
            f"| `{task['task_ref']}` | {task.get('proof_family')} | {task.get('difficulty')} "
            f"| `{sig_str}` | {pct(task['cohort_pass_rate'])} |"
        )
    lines.append("")

    if features.get("enrichment_present"):
        lines.append("## Failure modes by proof family")
        lines.append("")
        lines.append("Distribution of parsed Lean failure modes over enriched failing attempts.")
        lines.append("")
        family_modes: dict[str, Counter[str]] = defaultdict(Counter)
        for task in tasks:
            family = str(task.get("proof_family") or "unknown")
            for mode, count in (task.get("failure_modes") or {}).items():
                family_modes[family][mode] += count
        all_modes = sorted({m for counts in family_modes.values() for m in counts})
        header = "| Proof family | " + " | ".join(mode_labels.get(m, m) for m in all_modes) + " |"
        lines.append(header)
        lines.append("| --- | " + " | ".join("---" for _ in all_modes) + " |")
        for family in sorted(family_modes):
            counts = family_modes[family]
            row = " | ".join(str(counts.get(m, 0)) for m in all_modes)
            lines.append(f"| {family} | {row} |")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--features", type=Path, default=DEFAULT_FEATURES)
    parser.add_argument("--failure-modes", type=Path, default=DEFAULT_FAILURE_MODES)
    parser.add_argument("--out", type=Path, default=DEFAULT_REPORT)
    args = parser.parse_args()

    features = load_json(args.features)
    failure_modes = load_json(args.failure_modes)
    report = render_report(features, failure_modes)

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(report, encoding="utf-8")
    print(f"wrote {args.out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
